  const containerButtons = document.querySelectorAll('.table__button-hero');
  const modal = document.querySelector('.modal-hero');
  
  function showPicture(src = '') {
    
    let img = modal?.querySelector('img');
    img.src = src;
    modal?.classList.remove('visually_hidden');
    modal?.classList.add('modal_show');
  }
  
  // проходишься по массиву найденых кнопок
  containerButtons.forEach(elem => {
    const elemDataId = elem.getAttribute('data-id') || null; // вытаскиваещь значение дата атрибута
    const imgSrc =
      links.find(({ dataId }) => dataId === elemDataId)?.src || // проходишься по массиву с данными , в нем ищещь картинку у которой dataId  как у кнопки data-id
      '/ссылка на ТАКОЙ КАРТИНКИ НЕ НАЙДЕНО'; // добавляешь ссылку на заглушуку если такой картинки у тебя в массиве нет
    elem.addEventListener('click', () => showPicture(imgSrc)); // добавляешь эвент на кнопку в котором прокидываешь в функцию параметр src для картинки
  });

